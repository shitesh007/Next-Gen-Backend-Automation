// MAIN EXPORT: generateProject(schema)
// ============================================
export async function generateProject(schema) {
  const { projectName, models } = schema;
  const outDir = path.join(__dirname, `generated_${projectName.toLowerCase()}`);
  const generatedFiles = [];

  // Separate User model from entity models
  let userModel = models.find(m => m.name.toLowerCase() === 'user');
  const entityModels = models.filter(m => m.name.toLowerCase() !== 'user');

  // If no User model was provided, create a default one
  if (!userModel) {
    userModel = {
      name: 'User',
      fields: [
        { name: 'username', type: 'String', required: true },
        { name: 'email', type: 'String', required: true },
        { name: 'password', type: 'String', required: true },
        { name: 'role', type: 'String', required: false },
      ],
    };
  }

  // Create directory structure
  for (const sub of ['', 'models', 'routes', 'middleware', 'controllers']) {
    await fs.mkdir(path.join(outDir, sub), { recursive: true });
  }

  // --- Write root files ---
  const rootFiles = [
    ['package.json', tplPackageJson(projectName)],
    ['server.js', tplServer(entityModels)],
    ['.env', tplEnv()],
    [path.join('middleware', 'authMiddleware.js'), tplAuthMiddleware()],
    [path.join('controllers', 'authController.js'), tplAuthController()],
    [path.join('routes', 'auth.js'), tplAuthRoutes()],
  ];
  for (const [name, content] of rootFiles) {
    await fs.writeFile(path.join(outDir, name), content);
    generatedFiles.push(name);
  }

  // --- Write User model ---
  const userModelFile = 'models/userModel.js';
  await fs.writeFile(path.join(outDir, userModelFile), tplUserModel(userModel));
  generatedFiles.push(userModelFile);

  // --- Write entity models & routes (scoped to user) ---
  for (const model of entityModels) {
    const mFile = `models/${model.name.toLowerCase()}Model.js`;
    const rFile = `routes/${pluralize(model.name)}.js`;
    await fs.writeFile(path.join(outDir, mFile), tplEntityModel(model));
    await fs.writeFile(path.join(outDir, rFile), tplEntityRoutes(model.name));
    generatedFiles.push(mFile, rFile);
  }

  console.log(`✅ Generated ${generatedFiles.length} files in generated_${projectName.toLowerCase()}/`);
  return generatedFiles;
}
