// Normalize fields: supports both array [{name,type,required}] and object {name:Type}
function normalizeFields(fields) {
  if (Array.isArray(fields)) return fields;
  return Object.entries(fields).map(([name, type]) => ({ name, type, required: true }));
}

// --- User Model (with bcrypt + matchPassword) ---
const tplUserModel = (model) => {
  const fieldList = normalizeFields(model.fields);
  const fields = fieldList.map((f) => {
    const extras = f.name === 'password' ? ', select: false' : f.name === 'email' ? ', unique: true' : '';
    return `  ${f.name}: { type: ${f.type}, required: ${f.required !== false}${extras} }`;
  }).join(',\n');

  return `import mongoose from 'mongoose';
import bcrypt from 'bcryptjs';

const schema = new mongoose.Schema({
${fields}
}, { timestamps: true });

// Hash password before saving
schema.pre('save', async function(next) {
  if (!this.isModified('password')) return next();
  const salt = await bcrypt.genSalt(10);
  this.password = await bcrypt.hash(this.password, salt);
  next();
});

// Compare entered password with hashed password
schema.methods.matchPassword = async function(enteredPassword) {
  return await bcrypt.compare(enteredPassword, this.password);
};

export default mongoose.model('${model.name}', schema);
`;
};

// --- Entity Model (non-User — auto-injects user ownership ref) ---
const tplEntityModel = (model) => {
  const fieldList = normalizeFields(model.fields);

  // Filter out any 'user' field that might already exist (we inject our own)
  const filteredFields = fieldList.filter(f => f.name !== 'user');

  const fields = filteredFields.map((f) => {
    return `  ${f.name}: { type: ${f.type}, required: ${f.required !== false} }`;
  }).join(',\n');

  return `import mongoose from 'mongoose';

const schema = new mongoose.Schema({
${fields},
  user: { type: mongoose.Schema.Types.ObjectId, ref: 'User', required: true }
}, { timestamps: true });

export default mongoose.model('${model.name}', schema);
`;
};

