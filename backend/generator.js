// MAIN: runGenerator (standalone CLI)
// ============================================
const runGenerator = async () => {
  try {
    console.log("🚀 Starting AutoMind Generator...");

    // Read Input JSON
    const rawData = await fs.readFile('./input.json', 'utf-8');
    const { projectName, models } = JSON.parse(rawData);

    const outputDir = path.join(process.cwd(), `generated_${projectName.toLowerCase()}`);

    // Separate User model from entity models
    let userModel = models.find(m => m.name.toLowerCase() === 'user');
    const entityModels = models.filter(m => m.name.toLowerCase() !== 'user');

    // If no User model was provided, create a default one
    if (!userModel) {
      userModel = {
        name: 'User',
        fields: { username: 'String', email: 'String', password: 'String', role: 'String' },
      };
    }

    // Create directory structure
    const dirs = [
      outputDir,
      path.join(outputDir, 'models'),
      path.join(outputDir, 'routes'),
      path.join(outputDir, 'middleware'),
      path.join(outputDir, 'controllers'),
    ];
    for (const dir of dirs) {
      await fs.mkdir(dir, { recursive: true });
    }

    // Write root files
    await fs.writeFile(path.join(outputDir, 'package.json'), getPackageJson(projectName));
    await fs.writeFile(path.join(outputDir, 'server.js'), getServerJs(entityModels));
    await fs.writeFile(path.join(outputDir, '.env'), getEnv());
    await fs.writeFile(path.join(outputDir, 'middleware', 'authMiddleware.js'), getAuthMiddleware());
    await fs.writeFile(path.join(outputDir, 'controllers', 'authController.js'), getAuthController());
    await fs.writeFile(path.join(outputDir, 'routes', 'auth.js'), getAuthRoutes());

    // Write User model
    await fs.writeFile(path.join(outputDir, 'models', 'userModel.js'), getUserModel(userModel));

    // Write entity models & routes (scoped to user)
    for (const model of entityModels) {
      const modelName = model.name.toLowerCase();
      await fs.writeFile(path.join(outputDir, 'models', `${modelName}Model.js`), getEntityModel(model));
      await fs.writeFile(path.join(outputDir, 'routes', `${pluralize(model.name)}.js`), getEntityRoutes(model.name));
    }

    console.log(`✅ API generated successfully in folder: generated_${projectName.toLowerCase()}`);
    console.log("👉 Next steps: cd into folder, run 'npm install', then 'npm start'");

  } catch (error) {
    console.error("❌ Generator failed:", error.message);
  }
};

runGenerator();
