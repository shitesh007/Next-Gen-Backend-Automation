// --- User Model (with bcrypt + matchPassword) ---
const getUserModel = (model) => {
  const fields = Object.entries(model.fields).map(([key, val]) => {
    if (key === 'password') return `  ${key}: { type: ${val}, required: true, select: false }`;
    if (key === 'email') return `  ${key}: { type: ${val}, required: true, unique: true }`;
    return `  ${key}: { type: ${val}, required: true }`;
  }).join(',\n');

  return `import mongoose from 'mongoose';
import bcrypt from 'bcryptjs';

const schema = new mongoose.Schema({
${fields}
}, { timestamps: true });

// Hash password before saving
schema.pre('save', async function(next) {
  if (!this.isModified('password')) return next();
  const salt = await bcrypt.genSalt(10);
  this.password = await bcrypt.hash(this.password, salt);
  next();
});

// Compare entered password with hashed password
schema.methods.matchPassword = async function(enteredPassword) {
  return await bcrypt.compare(enteredPassword, this.password);
};

export default mongoose.model('${model.name}', schema);
`;
};

// --- Entity Model (non-User — auto-injects user ownership ref) ---
const getEntityModel = (model) => {
  // Filter out any 'user' field that might already exist
  const filteredEntries = Object.entries(model.fields).filter(([key]) => key !== 'user');

  const fields = filteredEntries.map(([key, val]) => {
    return `  ${key}: { type: ${val}, required: true }`;
  }).join(',\n');

  return `import mongoose from 'mongoose';

const schema = new mongoose.Schema({
${fields},
  user: { type: mongoose.Schema.Types.ObjectId, ref: 'User', required: true }
}, { timestamps: true });

export default mongoose.model('${model.name}', schema);
`;
};

