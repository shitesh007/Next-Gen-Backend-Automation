export default async function handler(req, res) {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');

  if (req.method === 'OPTIONS') return res.status(200).end();
  if (req.method !== 'POST') return res.status(405).json({ message: 'Method not allowed' });

  const { schema } = req.body;
  if (!schema || !schema.projectName) return res.status(400).json({ message: 'Valid schema is required' });

  try {
    const { projectName, models } = schema;
    const generatedFiles = {};

    let userModel = models.find(m => m.name.toLowerCase() === 'user');
    const entityModels = models.filter(m => m.name.toLowerCase() !== 'user');

    if (!userModel) {
      userModel = {
        name: 'User',
        fields: [
          { name: 'username', type: 'String', required: true },
          { name: 'email', type: 'String', required: true },
          { name: 'password', type: 'String', required: true },
          { name: 'role', type: 'String', required: false },
        ],
      };
    }

    // Generate all files in memory (no filesystem in serverless)
    generatedFiles['package.json'] = tplPackageJson(projectName);
    generatedFiles['server.js'] = tplServer(entityModels);
    generatedFiles['.env'] = tplEnv();
    generatedFiles['.gitignore'] = tplGitignore();
    generatedFiles['middleware/authMiddleware.js'] = tplAuthMiddleware();
    generatedFiles['controllers/authController.js'] = tplAuthController();
    generatedFiles['routes/auth.js'] = tplAuthRoutes();
    generatedFiles['models/userModel.js'] = tplUserModel(userModel);

    for (const model of entityModels) {
      generatedFiles[`models/${model.name.toLowerCase()}Model.js`] = tplEntityModel(model);
      generatedFiles[`routes/${pluralize(model.name)}.js`] = tplEntityRoutes(model.name);
    }

    return res.json({
      files: Object.keys(generatedFiles),
      fileContents: generatedFiles,
      projectName,
      message: 'API generated successfully!',
    });
  } catch (err) {
    console.error('Build error:', err.message);
    return res.status(500).json({ message: 'Build failed: ' + err.message });
  }
}
