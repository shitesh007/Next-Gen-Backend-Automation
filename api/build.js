const tplAuthMiddleware = () => `import jwt from 'jsonwebtoken';

export const protect = (req, res, next) => {
  let token;
  if (req.headers.authorization?.startsWith('Bearer')) {
    try {
      token = req.headers.authorization.split(' ')[1];
      const decoded = jwt.verify(token, process.env.JWT_SECRET || 'automind_secret_123');
      req.user = decoded;
      next();
    } catch (err) {
      res.status(401).json({ message: 'Not authorized, token failed' });
    }
  } else {
    res.status(401).json({ message: 'Not authorized, no token provided' });
  }
};
`;

const tplServer = (entityModels) => {
  const routeImports = entityModels.map(
    (m) => `import ${m.name.toLowerCase()}Routes from './routes/${pluralize(m.name)}.js';`
  ).join('\n');
  const routeMounts = entityModels.map(
    (m) => `app.use('/api/${pluralize(m.name)}', ${m.name.toLowerCase()}Routes);`
  ).join('\n');

  return `import express from 'express';
import mongoose from 'mongoose';
import cors from 'cors';
import helmet from 'helmet';
import morgan from 'morgan';
import dotenv from 'dotenv';

import authRoutes from './routes/auth.js';
${routeImports}

dotenv.config();

const app = express();

app.use(helmet());
app.use(cors());
app.use(express.json());
app.use(morgan('dev'));

app.use('/api/auth', authRoutes);
${routeMounts}

app.get('/', (req, res) => res.json({ status: 'ok', message: 'API is running — Generated by AutoMind' }));

const PORT = process.env.PORT || 5000;

mongoose.connect(process.env.MONGO_URI || 'mongodb://127.0.0.1:27017/generated-db')
  .then(() => {
    console.log('✅ MongoDB Connected');
    app.listen(PORT, () => console.log(\`🚀 Server running on port \${PORT}\`));
  })
  .catch(err => console.error('❌ DB Connection Error:', err));
`;
};

const tplUserModel = (model) => {
  const fieldList = normalizeFields(model.fields);
  const fields = fieldList.map((f) => {
    const extras = f.name === 'password' ? ', select: false' : f.name === 'email' ? ', unique: true' : '';
    return `  ${f.name}: { type: ${f.type}, required: ${f.required !== false}${extras} }`;
  }).join(',\n');

  return `import mongoose from 'mongoose';
import bcrypt from 'bcryptjs';

const schema = new mongoose.Schema({
${fields}
}, { timestamps: true });

schema.pre('save', async function(next) {
  if (!this.isModified('password')) return next();
  const salt = await bcrypt.genSalt(10);
  this.password = await bcrypt.hash(this.password, salt);
  next();
});

schema.methods.matchPassword = async function(enteredPassword) {
  return await bcrypt.compare(enteredPassword, this.password);
};

export default mongoose.model('${model.name}', schema);
`;
};

const tplEntityModel = (model) => {
  const fieldList = normalizeFields(model.fields);
  const filteredFields = fieldList.filter(f => f.name !== 'user');
  const fields = filteredFields.map((f) => {
    return `  ${f.name}: { type: ${f.type}, required: ${f.required !== false} }`;
  }).join(',\n');

  return `import mongoose from 'mongoose';

const schema = new mongoose.Schema({
${fields},
  user: { type: mongoose.Schema.Types.ObjectId, ref: 'User', required: true }
}, { timestamps: true });

export default mongoose.model('${model.name}', schema);
`;
};

const tplAuthController = () => `import jwt from 'jsonwebtoken';
import User from '../models/userModel.js';

const generateToken = (user) => {
  return jwt.sign(
    { id: user._id, email: user.email, role: user.role || 'user' },
    process.env.JWT_SECRET || 'automind_secret_123',
    { expiresIn: '7d' }
  );
};

export const register = async (req, res) => {
  try {
    const { username, email, password, role } = req.body;
    const existingUser = await User.findOne({ email });
    if (existingUser) return res.status(400).json({ message: 'User already exists with this email' });
    const user = await User.create({ username, email, password, role: role || 'user' });
    res.status(201).json({ _id: user._id, username: user.username, email: user.email, role: user.role, token: generateToken(user) });
  } catch (error) { res.status(400).json({ message: error.message }); }
};

export const login = async (req, res) => {
  try {
    const { email, password } = req.body;
    if (!email || !password) return res.status(400).json({ message: 'Please provide email and password' });
    const user = await User.findOne({ email }).select('+password');
    if (!user) return res.status(401).json({ message: 'Invalid email or password' });
    const isMatch = await user.matchPassword(password);
    if (!isMatch) return res.status(401).json({ message: 'Invalid email or password' });
    res.json({ _id: user._id, username: user.username, email: user.email, role: user.role, token: generateToken(user) });
  } catch (error) { res.status(500).json({ message: error.message }); }
};
`;

const tplAuthRoutes = () => `import express from 'express';
import { register, login } from '../controllers/authController.js';

const router = express.Router();

router.post('/register', register);
router.post('/login', login);

export default router;
`;

const tplEntityRoutes = (name) => `import express from 'express';
import Model from '../models/${name.toLowerCase()}Model.js';
import { protect } from '../middleware/authMiddleware.js';

const router = express.Router();

router.get('/', protect, async (req, res) => {
  try { const data = await Model.find({ user: req.user.id }).sort({ createdAt: -1 }); res.json(data); }
  catch (error) { res.status(500).json({ message: error.message }); }
});

router.get('/:id', protect, async (req, res) => {
  try { const data = await Model.findOne({ _id: req.params.id, user: req.user.id }); if (!data) return res.status(404).json({ message: '${name} not found' }); res.json(data); }
  catch (error) { res.status(500).json({ message: error.message }); }
});

router.post('/', protect, async (req, res) => {
  try { const data = await Model.create({ ...req.body, user: req.user.id }); res.status(201).json(data); }
  catch (error) { res.status(400).json({ message: error.message }); }
});

router.put('/:id', protect, async (req, res) => {
  try { const data = await Model.findOneAndUpdate({ _id: req.params.id, user: req.user.id }, req.body, { new: true, runValidators: true }); if (!data) return res.status(404).json({ message: '${name} not found or not authorized' }); res.json(data); }
  catch (error) { res.status(400).json({ message: error.message }); }
});

router.delete('/:id', protect, async (req, res) => {
  try { const data = await Model.findOneAndDelete({ _id: req.params.id, user: req.user.id }); if (!data) return res.status(404).json({ message: '${name} not found or not authorized' }); res.json({ message: '${name} deleted successfully' }); }
  catch (error) { res.status(500).json({ message: error.message }); }
});

export default router;
`;

